# entrada das referencias for

print("tentativa {} de {}" . format (3, 10))

print("tentativa {} de {}" . format (3, 10))

# inverte entrada

print("tentativa {1} de {0}" . format (3, 10))

# formatação do tipo float

print("R$ {}". format (1.59))

print("R$ {:f}". format (1.59))

print("R$ {:2f}". format (1.59))

print("R$ {:.2f}". format (1.59))

print("R$ {:7.2f}". format (1.59))

print("R$ {:7.2f}". format (4.5))

print("R$ {:07.2f}". format (1.59))

print("R$ {:08.3f}". format (1.59))

# formatação inteiro

print("R$ {:07d}". format (4))

print("R$ {:07d}". format (46))

# data

print("Data {:02d}/{:02d}". format (9,11)) 